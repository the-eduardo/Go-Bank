type eqCreateUserParamsMatcher struct {
	arg      db.CreateUserParams
	password string
}

func (e eqCreateUserParamsMatcher) Matches(x interface{}) bool {
	arg, ok := x.(db.CreateUserParams)
	if !ok {
		log.Println("arg is not of type db.CreateUserParams:", arg)
		return false
	}
	passwordMatches := util.CheckPassword(e.password, arg.HashedPassword) == nil
	if !passwordMatches {
		log.Println("password does not match:", arg.HashedPassword, ", e.password)", e.password)
		return false
	}

	// Temporarily ignore HashedPassword for deep equal check.
	tempArg := e.arg
	tempArg.HashedPassword = arg.HashedPassword

	return reflect.DeepEqual(tempArg, arg)
}
func (e eqCreateUserParamsMatcher) String() string {
	return fmt.Sprintf("matches arg %v and password %v", e.arg, e.password)
}
func EqCreateUserParams(arg db.CreateUserParams, password string) interface{} {
	return eqCreateUserParamsMatcher{arg: arg, password: password}
}

